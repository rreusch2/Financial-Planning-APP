def categorize_transaction(transaction_name):
    """
    Categorizes a transaction based on merchant name matching.
    Returns tuple of (category, subcategory) or None if no match found.
    """
    # Convert transaction name to lowercase for case-insensitive matching
    name_lower = transaction_name.lower()
    
    # Try exact match first
    if transaction_name in CUSTOM_CATEGORY_MAP:
        return CUSTOM_CATEGORY_MAP[transaction_name]
        
    # Try partial match on lowercase name
    for merchant, category in CUSTOM_CATEGORY_MAP.items():
        if merchant.lower() in name_lower:
            return category
            
    # Default category if no match found
    return ('Uncategorized', 'Other')